def display_bitmap_with_message(bitmap, message):
    if not message:
        sys.exit("Message is empty. Exiting...")

    for line in bitmap.splitlines():
        display_line = ''.join(
            [message[i % len(message)] if bit != ' ' else ' ' for i, bit in enumerate(line)])
        print(display_line)


if __name__ == "__main__":
    print('Enter the message to display with the bitmap.')
    user_message = input('> ')
    display_bitmap_with_message(bitmap, user_message)
